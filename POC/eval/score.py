"""
Aggregate the judge panel into the Day-3 answer-quality baseline.

Reads answers.jsonl (for strata) + judge_{gemini,gpt,claude}.jsonl and produces
eval/baseline_answers.md:
  - per-judge mean scores;
  - panel mean per dimension, stratified (in-corpus answer / out refuse / easy /
    difficult);
  - inter-judge agreement (mean pairwise |overall difference|);
  - self-preference delta: on Gemini-generated in-corpus answers, Gemini-judge's
    overall vs the GPT+Claude consensus (course's self-preference demo on our system).

    ~/venvs/ambassy-poc/bin/python eval/score.py
"""
import json
from pathlib import Path
from itertools import combinations

HERE = Path(__file__).resolve().parent
DIMS = ["correctness", "grounding", "behavior", "overall"]
JUDGES = ["gemini", "gpt", "claude"]


def load(name):
    p = HERE / name
    if not p.exists():
        return {}
    return {r["id"]: r for r in (json.loads(l) for l in p.read_text(encoding="utf-8").splitlines() if l.strip())}


def mean(xs):
    xs = [x for x in xs if x is not None]
    return sum(xs) / len(xs) if xs else None


def fmt(x):
    return f"{x:.2f}" if isinstance(x, (int, float)) else "—"


def main():
    answers = load("answers.jsonl")
    judges = {j: load(f"judge_{j}.jsonl") for j in JUDGES}
    present = [j for j in JUDGES if judges[j]]
    ids = [r["id"] for r in (json.loads(l) for l in (HERE / "answers.jsonl").read_text(encoding="utf-8").splitlines() if l.strip()) if r.get("answer")]

    rows = []
    for qid in ids:
        meta = answers[qid]
        rec = {"id": qid, "behavior": meta["expected_behavior"], "difficulty": meta["difficulty"],
               "in_corpus": meta["in_corpus"]}
        for j in present:
            s = judges[j].get(qid, {})
            for d in DIMS:
                rec[f"{j}_{d}"] = s.get(d)
        for d in DIMS:
            rec[f"panel_{d}"] = mean(rec.get(f"{j}_{d}") for j in present)
        rows.append(rec)

    def strat(subset, dim):
        return mean(r[f"panel_{dim}"] for r in subset)

    groups = {
        "ALL": rows,
        "in-corpus (answer)": [r for r in rows if r["in_corpus"]],
        "out-of-corpus (refuse)": [r for r in rows if not r["in_corpus"]],
        "easy": [r for r in rows if r["difficulty"] == "easy"],
        "difficult": [r for r in rows if r["difficulty"] == "difficult"],
    }

    L = ["# Baseline — Answer quality (3-judge panel) · 2026-06-10", "",
         f"Generator: gemini-2.5-pro · judges: {', '.join(present)} · rubric 1-5.", "",
         "## Panel mean by stratum", "",
         "| stratum | n | correctness | grounding | behavior | overall |",
         "|---|---|---|---|---|---|"]
    for name, sub in groups.items():
        if sub:
            L.append(f"| {name} | {len(sub)} | " + " | ".join(fmt(strat(sub, d)) for d in DIMS) + " |")

    L += ["", "## Per-judge mean overall", "",
          "| judge | mean overall |", "|---|---|"]
    for j in present:
        L.append(f"| {j} | {fmt(mean(r.get(f'{j}_overall') for r in rows))} |")

    # inter-judge agreement: mean pairwise |overall diff|
    if len(present) >= 2:
        diffs = []
        for r in rows:
            for a, b in combinations(present, 2):
                va, vb = r.get(f"{a}_overall"), r.get(f"{b}_overall")
                if va is not None and vb is not None:
                    diffs.append(abs(va - vb))
        L += ["", f"**Inter-judge agreement** (mean pairwise |overall difference|): "
              f"{fmt(mean(diffs))} (lower = more agreement)."]

    # self-preference: gemini judging gemini vs gpt+claude consensus, on answer questions
    others = [j for j in present if j != "gemini"]
    if "gemini" in present and others:
        sub = [r for r in rows if r["in_corpus"]]
        g = mean(r.get("gemini_overall") for r in sub)
        o = mean(mean(r.get(f"{j}_overall") for j in others) for r in sub)
        delta = (g - o) if (g is not None and o is not None) else None
        L += ["", "## Self-preference bias (course demo on our system)", "",
              f"On in-corpus answers (generated by gemini-2.5-pro): Gemini-judge overall = "
              f"{fmt(g)}, {'+'.join(others)} consensus = {fmt(o)}, **delta = {fmt(delta)}** "
              f"(positive ⇒ Gemini over-rates its own model's answers)."]

    L += ["", "## Per question", "",
          "| id | beh | " + " | ".join(f"{j} ov" for j in present) + " | panel ov |",
          "|---|---|" + "---|" * (len(present) + 1)]
    for r in rows:
        L.append(f"| {r['id']} | {r['behavior']} | " +
                 " | ".join(fmt(r.get(f'{j}_overall')) for j in present) +
                 f" | {fmt(r['panel_overall'])} |")

    report = "\n".join(L) + "\n"
    (HERE / "baseline_answers.md").write_text(report, encoding="utf-8")
    print(report)
    print(f"[written] {HERE / 'baseline_answers.md'}  (judges present: {present})")


if __name__ == "__main__":
    main()
